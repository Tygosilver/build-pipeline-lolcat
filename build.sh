#!/bin/bash
# Автоматическая остановка скрипта при любой ошибке команды
set -e

# ============================================================================
# Блок 1: Проверка аргументов и вывод справки
# ============================================================================

# Проверка наличия аргументов или запроса справки
if [[ "$1" == "--help" || "$1" == "-h" || -z "$1" ]]; then
    # Вывод справки с описанием режимов и примерами использования
    cat <<EOF
Использование: $0 --mode=<release|debug|coverage>

Описание режимов:
  release   - оптимизированная сборка для конечных пользователей
              (флаги компиляции: -O2 -DNDEBUG, удаление отладочной информации через strip)
  debug     - сборка с отладочными символами для разработчиков
              (флаги компиляции: -O0 -g, без strip)
  coverage  - сборка с инструментированием для измерения покрытия кода тестами
              (флаги компиляции: --coverage -O0 -g, генерация отчётов через lcov)

Примеры запуска:
  $0 --mode=release
  $0 --mode=debug
  $0 --mode=coverage
EOF
    exit 0
fi

# Извлечение режима сборки из аргумента (удаление префикса --mode=)
MODE_FLAG="$1"
MODE="${MODE_FLAG#--mode=}"

# Проверка корректности режима сборки (только допустимые значения)
if [[ ! "$MODE" =~ ^(release|debug|coverage)$ ]]; then
    echo "Ошибка: неизвестный режим '$MODE'. Допустимые значения: release, debug, coverage"
    exit 1
fi

echo "Запуск сборки в режиме: $MODE"

# ============================================================================
# Блок 2: Проверка наличия и сборка Docker-образа
# ============================================================================

# Имя образа для среды сборки
IMAGE_NAME="build-env:latest"

# Проверка существования образа в локальном репозитории Docker
# docker image inspect возвращает 0 при успехе, 1 при отсутствии образа
if docker image inspect "$IMAGE_NAME" &>/dev/null; then
    echo "Docker-образ найден: $IMAGE_NAME"
else
    echo "Docker-образ не найден, собираем из Dockerfile..."
    # Сборка образа из текущего каталога (точка означает контекст сборки)
    docker build -t "$IMAGE_NAME" .
    echo "Образ собран: $IMAGE_NAME"
fi

# ============================================================================
# Блок 3: Получение метаданных сборки
# ============================================================================

# Получение номера ревизии как количества коммитов в истории проекта lolcat
# git rev-list --count HEAD возвращает число коммитов от HEAD до корня истории
# Если команда завершается ошибкой (например, не репозиторий), возвращается 0
REVISION=$(cd src/lolcat && git rev-list --count HEAD 2>/dev/null || echo "0")

# Формирование временной метки в формате ГГГГММДД_ЧЧММСС для уникальной идентификации сборки
TIMESTAMP=$(date +%Y%m%d_%H%M%S)

# Формирование уникального идентификатора сборки на основе времени и ревизии
BUILD_ID="build_${TIMESTAMP}_rev${REVISION}"

echo "Ревизия проекта: $REVISION"
echo "Идентификатор сборки: $BUILD_ID"

# ============================================================================
# Блок 4: Запуск контейнера для выполнения сборки
# ============================================================================

echo "Запуск контейнера для сборки..."

# Запуск контейнера с пробросом текущей директории как /work внутри контейнера
# Флаги:
#   --rm        — автоматическое удаление контейнера после завершения
#   -v          — проброс тома (хост:контейнер)
#   -e          — передача переменных окружения в контейнер
docker run --rm \
    -v "$(pwd)":/work \
    -e REVISION="$REVISION" \
    -e BUILD_ID="$BUILD_ID" \
    "$IMAGE_NAME" \
    bash -c "/work/scripts/build_${MODE}.sh"

# ============================================================================
# Блок 5: Исправление прав доступа к артефактам
# ============================================================================

# Файлы, созданные внутри контейнера, принадлежат пользователю root хост-системы.
# Исправляем права для удобства работы на хост-машине без постоянного использования sudo.
if [[ -d artifacts && $(ls -A artifacts 2>/dev/null | wc -l) -gt 0 ]]; then
    echo "Исправление прав на артефакты..."
    # Присваиваем владение текущему пользователю (переменная $USER)
    # || true — игнорирование ошибки, если пользователь не в группе sudo
    sudo chown -R $USER:$USER artifacts/ 2>/dev/null || true
fi

if [[ -d reports && $(ls -A reports 2>/dev/null | wc -l) -gt 0 ]]; then
    sudo chown -R $USER:$USER reports/ 2>/dev/null || true
fi

# ============================================================================
# Блок 6: Формирование отчёта о сборке
# ============================================================================

# Имя файла отчёта с временной меткой для уникальности
REPORT_FILE="reports/build_report_${TIMESTAMP}.txt"

# Получение значения покрытия из файла last_coverage.txt (создаётся в режиме coverage)
# Если файл не существует или недоступен, возвращается "N/A"
# ВАЖНО: чтение происходит ПОСЛЕ исправления прав, чтобы избежать "Permission denied"
COVERAGE_VALUE=$(cat "reports/last_coverage.txt" 2>/dev/null || echo "N/A")

# Расчёт номера запуска как количество существующих отчётов + 1
# ls ... | wc -l возвращает число файлов, затем инкрементируем
RUN_NUMBER=$(ls reports/build_report_*.txt 2>/dev/null | wc -l)
RUN_NUMBER=$((RUN_NUMBER + 1))

# Создание файла отчёта с полной информацией о сборке в требуемом формате
cat > "$REPORT_FILE" <<EOF
Номер запуска: $RUN_NUMBER
Уникальный номер ревизии: $REVISION
Тип сборки: $MODE
Значение покрытия: $COVERAGE_VALUE
Дата и время: $(date '+%Y-%m-%d %H:%M:%S')
Артефакты: artifacts/lolcat_*-rev${REVISION}.*
EOF

echo ""
echo "Сборка завершена!"
echo "Отчёт сохранён: $REPORT_FILE"
echo "Артефакты в папке: artifacts/"